  /// Get all available variants for an icon name
  static List<String> getVariants(String iconName) {
    return [
      'bold/$iconName',
      'outline/$iconName',
      'linear/$iconName',
      'bulk/$iconName',
      'twotone/$iconName',
      'broken/$iconName',
    ];
  }

  /// Check if an icon ID is valid
  static bool isValidIconId(String iconId) {
    final parts = iconId.split('/');
    if (parts.length != 2) return false;

    final variant = parts[0];
    final validVariants = [
      'bold',
      'outline',
      'linear',
      'bulk',
      'twotone',
      'broken'
    ];

    return validVariants.contains(variant);
  }
